// abs() - returns the absolute value
console.log(Math.abs(-4)); // 4

// round() - rounds to the nearest integer
console.log(Math.round(3.4)); // 3
console.log(Math.round(3.6)); // 4

// ceil() - rounds upwards to the nearest integer
console.log(Math.ceil(3.2)); // 4
console.log(Math.ceil(3.3)); // 4
console.log(Math.ceil(3.5)); // 4
console.log(Math.ceil(3.6)); // 4
console.log(Math.ceil(3.7)); // 4

// floor() - rounds downwards to the nearest integer
console.log(Math.floor(3.2)); // 3
console.log(Math.floor(3.3)); // 3
console.log(Math.floor(3.5)); // 3
console.log(Math.floor(3.6)); // 3
console.log(Math.floor(3.7)); // 3

// exp() - returns the exponential value
// 2.718281828459045 to the power of number you passed
console.log(Math.exp(1)); //
console.log(Math.exp(3)); //
console.log(Math.exp(4)); //

// max() - returns the highest value
console.log(Math.max(1, 2, 3)); // 3

// min() - returns the lowest value
console.log(Math.min(1, 2, 3)); // 1

// sqrt() - returns the square root of passed number
console.log(Math.sqrt(100)); // 10

